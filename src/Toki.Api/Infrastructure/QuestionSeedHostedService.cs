/// <summary>
/// Seeds 1000+ quiz questions on startup if fewer than 100 exist.
/// Generates questions from:
///   1. Handcrafted blocks (~300) across 10 categories
///   2. Pairwise "prefer A or B" questions from 46 activities (~1035)
/// Total: ~1300 unique questions.
/// </summary>
public sealed class QuestionSeedHostedService : IHostedService
{
    private readonly IMongoDatabase _db;
    public QuestionSeedHostedService(IMongoDatabase db) => _db = db;

    public async Task StartAsync(CancellationToken ct)
    {
        var col   = _db.GetCollection<Question>("questions");
        var count = await col.CountDocumentsAsync(FilterDefinition<Question>.Empty, cancellationToken: ct);
        if (count >= 100) return;

        await col.DeleteManyAsync(FilterDefinition<Question>.Empty, ct);

        var all = new List<Question>();
        all.AddRange(HandcraftedQuestions());
        all.AddRange(GeneratePairQuestions());

        // shuffle
        var rng = new Random(42);
        all = all.OrderBy(_ => rng.Next()).ToList();

        await col.InsertManyAsync(all, cancellationToken: ct);
    }

    public Task StopAsync(CancellationToken ct) => Task.CompletedTask;

