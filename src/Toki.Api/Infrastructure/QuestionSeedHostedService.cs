            "играть в шахматы", "собирать пазлы", "вязать/шить", "лепить из глины",
            "садоводство", "волонтёрить", "посещать выставки", "ходить в кафе",
            "готовить выпечку", "изучать историю", "астрономия как хобби",
        };

        var list = new List<Question>();
        for (var i = 0; i < items.Length; i++)
            for (var j = i + 1; j < items.Length; j++)
                list.Add(new Question
                {
                    Text     = $"Что вам нравится больше: {items[i]} или {items[j]}?",
                    Category = "preferences",
                    Options  = new List<string> { items[i].UpperFirst(), items[j].UpperFirst(), "Оба одинаково", "Ни то, ни другое" },
                });

        return list;
    }

    // ── Helper ──────────────────────────────────────────────────────────────
    static IEnumerable<Question> Cat(string category, IEnumerable<(string text, string[] opts)> items) =>
        items.Select(t => new Question { Text = t.text, Category = category, Options = t.opts.ToList() });
}

file static class StringExtensions
{
    public static string UpperFirst(this string s) =>
        string.IsNullOrEmpty(s) ? s : char.ToUpper(s[0]) + s[1..];
}
